#endswith() Retorna verdadeiro se a string terminar com o valor especificado
texto = "Aula nove!"

resultado1 = texto.endswith("nove!")  # Verifica se termina com "mundo!"
resultado2 = texto.endswith("nove!")     # Verifica se termina com "Olá"

print(resultado1)  # Saída: True
print(resultado2)  # Saída: False
