#rfind() Pesquisa na string um valor especificado e retorna a última posição de onde ele foi encontrado
frase = "Python é uma linguagem de programação, e Python é divertido"

# Usando o método rfind() para encontrar a última ocorrência da palavra "Python"
ultima_ocorrencia = frase.rfind("Python")

print("Última ocorrência:", ultima_ocorrencia)  # Saída: 41